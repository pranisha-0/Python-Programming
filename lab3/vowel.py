#vowel or consonant check
let = input("Enter a letter: ")
if let == "a" or let == "e" or let == "i" or let == "o" or let == "u":
    print(f"{let} is a vowel.")
elif let == "A" or let == "E" or let == "I" or let == "O" or let == "U":
        print(f"{let} is a vowel.")
else:
    print(f"{let} is a consonant.")
